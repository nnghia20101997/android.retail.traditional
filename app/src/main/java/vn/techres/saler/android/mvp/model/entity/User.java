package vn.techres.saler.android.mvp.model.entity;

public class User {
    private int id = 0;
    private String first_name = "";
    private String last_name = "";
    private String full_name = "";
    private String email = "";
    private String contact_note = "";
    private String avatar = "";
    private String phone_number = "";
    private String access_token = "";
    private String token_type = "Bearer";
    private String user_type = "sell";

    public static User getUserDefault() {
        User user = new User();
        user.setId(0);
        user.setAccess_token("");
        return user;
    }

    public String getUser_type() {
        return user_type;
    }

    public void setUser_type(String user_type) {
        this.user_type = user_type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getFull_name() {
        return full_name;
    }

    public void setFull_name(String full_name) {
        this.full_name = full_name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getContact_note() {
        return contact_note;
    }

    public void setContact_note(String contact_note) {
        this.contact_note = contact_note;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getPhone_number() {
        return phone_number;
    }

    public void setPhone_number(String phone_number) {
        this.phone_number = phone_number;
    }

    public String getAccess_token() {
        return access_token;
    }

    public void setAccess_token(String access_token) {
        this.access_token = access_token;
    }

    public String getToken_type() {
        return token_type;
    }

    public void setToken_type(String token_type) {
        this.token_type = token_type;
    }
}
